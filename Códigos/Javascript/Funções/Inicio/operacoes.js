let valor1=5
let valor2=8

function soma(valor1,valor2){
    let somatotal=valor1+valor2
    return somatotal
}

console.log("Soma de "+valor1+" e "+valor2+ " -> "+soma(valor1,valor2));

function subtracao(valor1,valor2){
    let subtotal=valor1 - valor2
    return subtotal
}

console.log("Subtração de "+valor1+" e "+valor2+" -> "+subtracao(valor1,valor2));

function multiplicacao(valor1,valor2){
    let multitotal=valor1*valor2
    return multitotal
}

console.log("Multiplicação de "+valor1+" e "+valor2+" -> "+multiplicacao(valor1,valor2));